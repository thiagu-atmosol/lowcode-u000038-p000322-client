function contact(event, contactUsId) {
    event.preventDefault();
    console.log(contactUsId, "this is the id of contact us section passed.");

    const contactUsSectionDiv = document.getElementById('section'+contactUsId);
    const firstName = contactUsSectionDiv.querySelector('#contact-firstName').value;
    const lastName = firstName.split(" ")[1];
    const email = contactUsSectionDiv.querySelector('#contact-email').value;
    const message = contactUsSectionDiv.querySelector('#contact-message').value;

    if ((firstName || lastName) && email && message) {
        console.log(firstName, "First Name");
        console.log(lastName, "Last Name");
        console.log(email, "Email");
        console.log(message, "Message");

        sendEmail(event, {
            firstName,
            lastName,
            email,
            message
        }, contactUsSectionDiv);
    } else {
        displayMessage('Please fill in all fields.', 'error');
    }
}

function sendEmail(event, data, contactUsSectionDiv) {
    event.preventDefault();
    const baseUrl = "https://lowcodedev.azurewebsites.net/api";
    const authToken = "eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJzdWIiOiIzOCIsImVtYWlsIjoidGVzdGhrQHlvcG1haWwuY29tIiwiaWQiOiIzOCIsImV4cCI6MTcyNDM0MTEyNX0.o1TBZmb9kOHhO9WxkCudottGDvnEbrigiw1USuslVgA";
    const userId = 38;
    const projectId = 322;

    const route = `/${userId}/Project/${projectId}/ContatUs`;
    const url = `${baseUrl}${route}`;

    fetch(url, {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json',
            'Authorization': `Bearer ${authToken}`
        },
        body: JSON.stringify(data)
    })
    .then(response => {
        if (response.ok) { // Check if response is OK
            displayMessage('Email sent successfully!', 'success');
            resetContactUsFields(contactUsSectionDiv); // Reset the form here
        } else {
            return response.json().then(data => {
                displayMessage(data.error || 'An error occurred.', 'error');
            });
        }
    })
    .catch(error => {
        console.error('Error:', error);
        displayMessage('An error occurred.', 'error');
    });
}


function displayMessage(message, type) {
    const responseMessageDiv = document.getElementById('responseMessage');
    responseMessageDiv.textContent = message;
    if (type === 'success') {
        responseMessageDiv.style.color = 'green';
    } else {
        responseMessageDiv.style.color = 'red';
    }
    setTimeout(() => {
        responseMessageDiv.textContent = '';
    }, 2000);
}

